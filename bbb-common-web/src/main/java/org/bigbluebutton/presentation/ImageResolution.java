package org.bigbluebutton.presentation;

public class ImageResolution {

	private final int width;
	private final int height;

	public ImageResolution(int width, int height) {
		this.width = width;
		this.height = height;
	}
	
	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
	 
}
